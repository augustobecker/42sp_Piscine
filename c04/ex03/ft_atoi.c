int	ft_atoi(char *str)
{
	int	sign;
	int	nb;
	int	i;

	sign = 1;
	nb = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		++i;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		++i;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = (str[i] - 48) + (nb * 10);
		++i;
	}
	return (nb * sign);
}
