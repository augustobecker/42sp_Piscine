int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 0)
		nb *= -1;
	i = 2;
	if (nb == 2 || nb == 3 || nb == 5 || nb == 7)
		return (1);
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= (nb / 2))
	{
		if (nb % i == 0 && i != nb)
			return (0);
		i += 2;
	}
	return (1);
}
